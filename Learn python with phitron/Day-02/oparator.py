#Python Arithmetic Operators
# +, -, * /, %
x = 15;
y = 2;
print(x + y) #sumation
print(x - y) #sub
print(x * y) #multiple 
print(x / y) #divition
print(x % y) #modules
print(x // y) # Floor Divition
print(x ** y) # Exponentiationw

# Python Comparison Operators
""" ==, !=, >, <, >=, <= """
a = 10;
b = 10;
print(a > b); # greater then 
print(a < b); #less then
print(a >= b); # greater then or equal to
print(a <= b); # less then or equal to
print(a != b); #not equal to 
print(a == b); # equal to 